    function mostrarOpciones_alumno() {
        let listaOpciones = document.getElementById("lista_opciones_alumno");
        if (listaOpciones.style.display === "none") {
            listaOpciones.style.display = "block";
        } else {
            listaOpciones.style.display = "none";
        }
    }

